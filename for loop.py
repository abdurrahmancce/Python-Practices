# for loop to iterate through a list
fruits=["apple", "banana", "cherry"]
for i in fruits:
    print(i)

print("\n")

for i in range(0,10):
    print(i)

print("\n")

for i in range(1,20,3):
    print(i)

print("\n")

for i in range(10,0,-1):
    print(i)

print("\n")

for i in range(2,10,+2):
    print(i)

print("\n")

for i in range(2,10):
    print(i*2)